def generate_uuid():
    """Generate a new UUID and display it"""
    unique_id = uuid.uuid4()
    uuid_str = str(unique_id)
    uuid_hex = unique_id.hex
    
    # Update the text fields
    uuid_entry.delete(0, tk.END)
    uuid_entry.insert(0, uuid_str)
    
    hex_entry.delete(0, tk.END)
    hex_entry.insert(0, uuid_hex)
    
    status_label.config(text="üî• New UUID generated by TECSO!", fg="#00ff87")

def copy_uuid():
    """Copy UUID to clipboard"""
    uuid_text = uuid_entry.get()
    if uuid_text:
        pyperclip.copy(uuid_text)
        status_label.config(text="üìã UUID copied to clipboard!", fg="#8b5cf6")
    else:
        messagebox.showwarning("Warning", "No UUID to copy!")

def copy_hex():
    """Copy UUID HEX to clipboard"""
    hex_text = hex_entry.get()
    if hex_text:
        pyperclip.copy(hex_text)
        status_label.config(text="üìã UUID HEX copied to clipboard!", fg="#8b5cf6")
    else:
        messagebox.showwarning("Warning", "No UUID HEX to copy!")

